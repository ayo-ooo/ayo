func TestGenerateTypes_InputOnly(t *testing.T) {
	inputSchema, _ := schema.ParseSchema([]byte(testutil.ValidInputSchema()))

	code, err := GenerateTypes(inputSchema, nil, "main")
	if err != nil {
		t.Fatalf("GenerateTypes() error = %v", err)
	}

	if !strings.Contains(code, "package main") {
		t.Error("Generated code should contain 'package main'")
	}

	if !strings.Contains(code, "type Input struct") {
		t.Error("Generated code should contain Input struct")
	}

	if strings.Contains(code, "type Output struct") {
		t.Error("Generated code should NOT contain Output struct")
	}

	if !strings.Contains(code, `json:"query"`) {
		t.Error("Generated code should contain JSON tag for query field")
	}
}

