func TestGenerateTypes_MultipleFields(t *testing.T) {
	schemaJSON := `{
		"type": "object",
		"properties": {
			"name": {"type": "string"},
			"age": {"type": "integer"},
			"score": {"type": "number"},
			"active": {"type": "boolean"},
			"tags": {"type": "array"},
			"metadata": {"type": "object"}
		}
	}`

	inputSchema, _ := schema.ParseSchema([]byte(schemaJSON))

	code, err := GenerateTypes(inputSchema, nil, "main")
	if err != nil {
		t.Fatalf("GenerateTypes() error = %v", err)
	}

	expectedFields := []struct {
		name     string
		goType   string
	}{
		{"Name", "string"},
		{"Age", "int"},
		{"Score", "float64"},
		{"Active", "bool"},
		{"Tags", "[]any"},
		{"Metadata", "map[string]any"},
	}

	for _, ef := range expectedFields {
		if !strings.Contains(code, ef.name+" "+ef.goType) {
			t.Errorf("Generated code missing field %s %s", ef.name, ef.goType)
		}
	}
}

func TestJsonTypeToGo(t *testing.T) {
	tests := []struct {
		input    string
		expected string
	}{
		{"string", "string"},
		{"integer", "int"},
		{"number", "float64"},
		{"boolean", "bool"},
		{"array", "[]any"},
		{"object", "map[string]any"},
		{"unknown", "any"},
		{"", "any"},
	}

	for _, tt := range tests {
		t.Run(tt.input, func(t *testing.T) {
			got := jsonTypeToGo(tt.input)
			if got != tt.expected {
				t.Errorf("jsonTypeToGo(%q) = %q, want %q", tt.input, got, tt.expected)
			}
		})
	}
}

