	// runModelSelectionTUI
	b.WriteString("// runModelSelectionTUI launches the model selection TUI\n")
	b.WriteString("func runModelSelectionTUI() error {\n")
	b.WriteString("\tproviders := getProviders()\n\n")
	b.WriteString("\t// Check if we have any providers with API keys\n")
	b.WriteString("\thasAvailable := false\n")
	b.WriteString("\tfor _, p := range providers {\n")
	b.WriteString("\t\tif hasAPIKey(string(p.ID)) {\n")
	b.WriteString("\t\t\thasAvailable = true\n")
	b.WriteString("\t\t\tbreak\n")
	b.WriteString("\t\t}\n")
	b.WriteString("\t}\n\n")
	b.WriteString("\tif !hasAvailable {\n")
	b.WriteString("\t\treturn fmt.Errorf(\"no API keys found - please set ANTHROPIC_API_KEY, OPENAI_API_KEY, or another provider's key\")\n")
	b.WriteString("\t}\n\n")
	b.WriteString("\ttui := newModelSelectionTUI()\n")
	b.WriteString("\tp := tea.NewProgram(tui, tea.WithAltScreen())\n")
	b.WriteString("\tmodel, err := p.Run()\n")
	b.WriteString("\tif err != nil {\n")
	b.WriteString("\t\treturn fmt.Errorf(\"running TUI: %w\", err)\n")
	b.WriteString("\t}\n\n")
	b.WriteString("\tfinalModel := model.(*modelSelectionTUI)\n")
	b.WriteString("\tif finalModel.quitting && (finalModel.selectedProvider == nil || finalModel.selectedModel == nil) {\n")
	b.WriteString("\t\tos.Exit(0)\n")
	b.WriteString("\t}\n\n")
	b.WriteString("\tif finalModel.selectedProvider == nil || finalModel.selectedModel == nil {\n")
	b.WriteString("\t\treturn fmt.Errorf(\"no selection made\")\n")
	b.WriteString("\t}\n\n")
	b.WriteString("\t// Save the selection\n")
	b.WriteString("\tcfg := &AgentConfig{\n")
	b.WriteString("\t\tProvider: string(finalModel.selectedProvider.provider.ID),\n")
	b.WriteString("\t\tModel:    finalModel.selectedModel.model.ID,\n")
	b.WriteString("\t\tAPIKey:   os.Getenv(getAPIKeyEnv(string(finalModel.selectedProvider.provider.ID))),\n")
	b.WriteString("\t}\n")
	b.WriteString("\treturn saveConfig(cfg)\n")
	b.WriteString("}\n")

	return b.String(), nil
