	var b strings.Builder

	b.WriteString("// Code generated by ayo. DO NOT EDIT.\n\n")
	b.WriteString(fmt.Sprintf("package %s\n\n", pkgName))

	b.WriteString("import (\n")
	b.WriteString("\t\"fmt\"\n")
	b.WriteString("\t\"os\"\n")
	b.WriteString("\t\"strings\"\n")
	b.WriteString("\n")
	b.WriteString("\t\"github.com/charmbracelet/bubbles/list\"\n")
	b.WriteString("\t\"github.com/charmbracelet/bubbletea\"\n")
	b.WriteString("\t\"github.com/charmbracelet/lipgloss\"\n")
	b.WriteString(")\n\n")

	b.WriteString("// TUI states\n")
	b.WriteString("type tuiState int\n\n")
	b.WriteString("const (\n")
	b.WriteString("\tstateProviderSelect tuiState = iota\n")
	b.WriteString("\tstateModelSelect\n")
